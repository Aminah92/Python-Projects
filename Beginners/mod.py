#Calculator module
#Function for Addition
def add(num1,num2):
	return num1 + num2
    
#Function for subtraction
def sub(num1,num2):
	return num1 - num2
    
#Function for Mutiplication
def mult(num1,num2):
	return num1 * num2

#Function for Division
def div(num1,num2):
	return num1 / num2
